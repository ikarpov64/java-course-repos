package com.volgir.homework.module2.third.ex5;

public class Animal {
    private int taleLength;
    private int bodyLength;

    public Animal(int taleLength, int bodyLength) {
        this.taleLength = taleLength;
        this.bodyLength = bodyLength;
    }

    public int getTaleLength() {
        return taleLength;
    }

    public int getBodyLength() {
        return bodyLength;
    }
}
